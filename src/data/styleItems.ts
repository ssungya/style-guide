export function getRecommendations(
  bodyType: StyleItem["bodyType"],
  gender: Gender = "unspecified",
) {
  return styleItems.filter((item) => {
    if (item.bodyType !== bodyType) return false;
    if (gender === "unspecified") return true;
    return item.gender === "unisex" || item.gender === gender;
  });
}
